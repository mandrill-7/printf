#include "libft.h"

int		ft_isprint(int c)
{
	if ((c >= ' ' && c <= 47) || (c >= '(' && c <= '/') ||
			(c >= '0' && c <= '?') || (c >= '@' && c <= 'G') ||
			(c >= 'H' && c <= 'O') || (c >= '@' && c <= '~'))
		return (1);
	return (0);
}
